function L = LgrC(c,order)

if order==2
    L(1,1)=1/(c(1)-c(2));      L(1,2)=1/(c(2)-c(1));
    L(2,1)=-c(2)/(c(1)-c(2));  L(2,2)=-c(1)/(c(2)-c(1)); 
   
elseif order==3
    L(1,1)=1/((c(1)-c(2))*(c(1)-c(3)));             L(1,2)=1/((c(2)-c(1))*(c(2)-c(3)));             L(1,3)=1/((c(3)-c(1))*(c(3)-c(2)));
    L(2,1)=-(c(2)+c(3))/((c(1)-c(2))*(c(1)-c(3)));  L(2,2)=-(c(1)+c(3))/((c(2)-c(1))*(c(2)-c(3)));  L(2,3)=-(c(1)+c(2))/((c(3)-c(1))*(c(3)-c(2)));
    L(3,1)=c(2)*c(3)/((c(1)-c(2))*(c(1)-c(3)));     L(3,2)=c(1)*c(3)/((c(2)-c(1))*(c(2)-c(3)));     L(3,3)=c(1)*c(2)/((c(3)-c(1))*(c(3)-c(2)));
    
elseif order==4  
    L(1,1)=1/((c(1)-c(2))*(c(1)-c(3))*(c(1)-c(4)));                                L(1,2)=1/((c(2)-c(1))*(c(2)-c(3))*(c(2)-c(4)));                                L(1,3)=1/((c(3)-c(1))*(c(3)-c(2))*(c(3)-c(4)));                                L(1,4)=1/((c(4)-c(1))*(c(4)-c(2))*(c(4)-c(3)));
    L(2,1)=(-c(2)-c(3)-c(4))/((c(1)-c(2))*(c(1)-c(3))*(c(1)-c(4)));                L(2,2)=(-c(1)-c(3)-c(4))/((c(2)-c(1))*(c(2)-c(3))*(c(2)-c(4)));                L(2,3)=(-c(1)-c(2)-c(4))/((c(3)-c(1))*(c(3)-c(2))*(c(3)-c(4)));                L(2,4)=(-c(1)-c(2)-c(3))/((c(4)-c(1))*(c(4)-c(2))*(c(4)-c(3)));
    L(3,1)=(c(2)*c(3)+c(2)*c(4)+c(3)*c(4))/((c(1)-c(2))*(c(1)-c(3))*(c(1)-c(4)));  L(3,2)=(c(1)*c(3)+c(1)*c(4)+c(3)*c(4))/((c(2)-c(1))*(c(2)-c(3))*(c(2)-c(4)));  L(3,3)=(c(1)*c(2)+c(1)*c(4)+c(2)*c(4))/((c(3)-c(1))*(c(3)-c(2))*(c(3)-c(4)));  L(3,4)=(c(1)*c(2)+c(1)*c(3)+c(2)*c(3))/((c(4)-c(1))*(c(4)-c(2))*(c(4)-c(3)));
    L(4,1)=-c(2)*c(3)*c(4)/((c(1)-c(2))*(c(1)-c(3))*(c(1)-c(4)));                  L(4,2)=-c(1)*c(3)*c(4)/((c(2)-c(1))*(c(2)-c(3))*(c(2)-c(4)));                  L(4,3)=-c(1)*c(2)*c(4)/((c(3)-c(1))*(c(3)-c(2))*(c(3)-c(4)));                  L(4,4)=(-c(1)*c(2)*c(3))/((c(4)-c(1))*(c(4)-c(2))*(c(4)-c(3)));
end

end

